package explan.model;

public class ExtendedSimulationParams {
    public double lambda1;
    public double mu1;
    public double lambda2;
    public double mu2;

    public ExtendedSimulationParams(double lambda1, double mu1, double lambda2, double mu2) {
        this.lambda1 = lambda1;
        this.mu1 = mu1;
        this.lambda2 = lambda2;
        this.mu2 = mu2;
    }
}
